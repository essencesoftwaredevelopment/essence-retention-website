import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..");

const htmlFiles = [
  "index.html",
  "privacy-policy/index.html",
  "data-deletion-instructions/index.html",
];

const generatedFiles = new Set([
  "assets/js/runtime-config.generated.js",
]);

const failures = [];
const warnings = [];

function stripHtmlComments(html) {
  return html.replace(/<!--[\s\S]*?-->/g, "");
}

function normalizeLocalTarget(value) {
  const trimmed = value.trim();
  if (
    !trimmed ||
    trimmed.startsWith("#") ||
    trimmed.startsWith("http://") ||
    trimmed.startsWith("https://") ||
    trimmed.startsWith("mailto:") ||
    trimmed.startsWith("tel:") ||
    trimmed.startsWith("data:") ||
    trimmed.startsWith("javascript:")
  ) {
    return null;
  }

  return trimmed.split("#")[0].split("?")[0];
}

async function existsAsFileOrDirectoryIndex(relativeTarget, fromFile) {
  const withoutLeadingSlash = relativeTarget.replace(/^\/+/, "");
  const baseDir = path.dirname(path.join(rootDir, fromFile));
  const candidates = [];

  if (!relativeTarget.startsWith("/") && withoutLeadingSlash) {
    candidates.push(path.resolve(baseDir, relativeTarget));
    candidates.push(path.resolve(baseDir, relativeTarget, "index.html"));
  } else if (withoutLeadingSlash) {
    candidates.push(path.join(rootDir, withoutLeadingSlash));
    candidates.push(path.join(rootDir, withoutLeadingSlash, "index.html"));
  }

  for (const candidate of candidates) {
    try {
      const stat = await fs.stat(candidate);
      if (stat.isFile()) return true;
    } catch {
      // Try the next candidate.
    }
  }

  return false;
}

async function checkHtmlFile(file) {
  const absolutePath = path.join(rootDir, file);
  const html = await fs.readFile(absolutePath, "utf8");
  const uncommented = stripHtmlComments(html);
  const ids = new Set();
  const duplicateIds = new Set();

  for (const match of uncommented.matchAll(/\bid=(["'])(.*?)\1/g)) {
    const id = match[2];
    if (ids.has(id)) duplicateIds.add(id);
    ids.add(id);
  }

  duplicateIds.forEach((id) => {
    failures.push(`${file}: duplicate id "${id}"`);
  });

  for (const match of uncommented.matchAll(/\sstyle=(["'])([\s\S]*?)\1/g)) {
    failures.push(`${file}: inline style attribute found near "${match[0].slice(0, 80)}"`);
  }

  for (const match of uncommented.matchAll(/\s(?:src|href)=(["'])(.*?)\1/g)) {
    const rawValue = match[2];

    if (rawValue.startsWith("#")) {
      const anchorId = rawValue.slice(1);
      if (anchorId && !ids.has(anchorId)) {
        failures.push(`${file}: href points to missing anchor "#${anchorId}"`);
      }
      continue;
    }

    const localTarget = normalizeLocalTarget(rawValue);
    if (!localTarget) continue;

    const normalizedTarget = localTarget.replace(/^\/+/, "");
    if (!normalizedTarget || generatedFiles.has(normalizedTarget)) continue;

    if (!(await existsAsFileOrDirectoryIndex(localTarget, file))) {
      failures.push(`${file}: missing local target "${rawValue}"`);
    }
  }

  const sectionOpenCount = (uncommented.match(/<section\b/g) || []).length;
  const sectionCloseCount = (uncommented.match(/<\/section>/g) || []).length;
  if (sectionOpenCount !== sectionCloseCount) {
    failures.push(`${file}: section tag mismatch (${sectionOpenCount} open, ${sectionCloseCount} close)`);
  }
}

for (const file of htmlFiles) {
  try {
    await checkHtmlFile(file);
  } catch (error) {
    failures.push(`${file}: ${error instanceof Error ? error.message : "could not be checked"}`);
  }
}

try {
  await fs.access(path.join(rootDir, "assets/js/runtime-config.generated.js"));
} catch {
  warnings.push("assets/js/runtime-config.generated.js is generated by npm run prepare:runtime-config.");
}

if (warnings.length) {
  console.log("Static site check warnings:");
  warnings.forEach((warning) => console.log(`- ${warning}`));
}

if (failures.length) {
  console.error("Static site check failed:");
  failures.forEach((failure) => console.error(`- ${failure}`));
  process.exit(1);
}

console.log("Static site check passed.");
